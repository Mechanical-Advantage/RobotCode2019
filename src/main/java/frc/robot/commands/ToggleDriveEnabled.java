package frc.robot.commands;

import frc.robot.Robot;

import edu.wpi.first.wpilibj.command.InstantCommand;
import edu.wpi.first.wpilibj.smartdashboard.SmartDashboard;

/**
 * Toggles whether drive is enabled when using handheld controller
 */
public class ToggleDriveEnabled extends InstantCommand {

  public ToggleDriveEnabled() {
    super();
    // Use requires() here to declare subsystem dependencies
    // eg. requires(chassis);
  }

  // Called once when the command executes
  @Override
  protected void initialize() {
    Robot.oi.toggleDriveEnabled();
    SmartDashboard.putBoolean("Drive Enabled", Robot.oi.getDriveEnabled());
  }

}
